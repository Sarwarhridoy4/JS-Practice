
let color = 'Yellow';


switch(color){

    case 'Yellow':
    console.log('You should Slow down your speed.');

    break;

    case 'Red':
    console.log('You Should Stop now.');

    break;

    case 'Green':
    console.log('You should move on now.');

    break;

    default:
    console.log('Always be careful while you are on the road.');


}